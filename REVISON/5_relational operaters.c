#include<stdio.h>

int main()
{
    int a=10,b=5;//Relational operaters return 1=true : 0=false;
    printf("\nequal         : %d",a==b);
    printf("\nnot equal     : %d",a!=b);
    printf("\ngreather than : %d",a>b);
    printf("\nless than     : %d",a<b);
    printf("\ngreather than or equal : %d",a>=b);
    printf("\nless than or equal     : %d",a<=b);
    return 0;
}
